async def convert_subtitle_to_srt(input_sub_path: Path):
    """Convert subtitle to SRT format with enhanced error handling."""
    from ..ext_utils.video_process_utils import generate_unique_filename

    try:
        if not input_sub_path or not input_sub_path.exists():
            LOGGER.error(f"Input subtitle file not found: {input_sub_path}")
            return None

        # Check if input is already SRT
        if input_sub_path.suffix.lower() == ".srt":
            LOGGER.debug("Input is already SRT format")
            return input_sub_path

        # Generate unique output path
        base_output = input_sub_path.with_suffix(".srt")
        out_path = Path(generate_unique_filename(str(base_output)))

        # Build conversion command with timeout and better parameters
        cmd = [
            BinConfig.FFMPEG_NAME,
            "-hide_banner",
            "-y",
            "-loglevel",
            "error",
            "-i",
            str(input_sub_path),
            "-c:s",
            "srt",
            str(out_path),
        ]

        LOGGER.debug(f"Converting subtitle {input_sub_path} to SRT format...")

        # Run with timeout to prevent hanging
        try:
            proc = await _create_memory_aware_subprocess(
                *cmd, stdout=PIPE, stderr=PIPE, max_retries=2, wait_for_resources=True
            )

            if not proc:
                LOGGER.error(
                    f"Failed to create subprocess for subtitle conversion: {input_sub_path}"
                )
                return None

            stdout, stderr = await asyncio.wait_for(
                proc.communicate(), timeout=120
            )  # 2 minute timeout
        except asyncio.TimeoutError:
            proc.kill()
            await proc.wait()
            LOGGER.error(f"SRT conversion timed out for {input_sub_path}")
            return None

        if proc.returncode == 0:
            # Validate output file
            if await aiopath.isfile(out_path):
                file_size = await aiopath.getsize(out_path)
                if file_size > 0:
                    LOGGER.debug(
                        f"Successfully converted to SRT: {out_path} ({file_size} bytes)"
                    )
                    return out_path
                else:
                    LOGGER.error("Converted SRT file is empty")
                    # Clean up empty file
                    try:
                        await aiopath.unlink(out_path)
                    except Exception:
                        pass
            else:
                LOGGER.error("Converted SRT file was not created")
        else:
            stderr_str = stderr.decode(errors="ignore")
            LOGGER.error(f"SRT conversion failed: {stderr_str}")

    except Exception as e:
        LOGGER.error(f"Error converting subtitle to SRT: {e}")
        # Clean up partial file if it exists
        try:
            if "out_path" in locals() and await aiopath.isfile(out_path):
                await aiopath.unlink(out_path)
        except Exception:
            pass

    return None


