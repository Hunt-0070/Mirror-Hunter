def HEX_TO_ASS(h):
    """Convert hex color to ASS subtitle format (BGR order)"""
    if re.match(r"^#?[0-9A-Fa-f]{6}$", h):
        # Remove # if present
        hex_color = h[1:] if h.startswith("#") else h
        # Convert RGB to BGR for ASS format
        return f"&H00{hex_color[4:6]}{hex_color[2:4]}{hex_color[0:2]}"
    else:
        # Use DEFAULT_COLOR instead of defaulting to white
        default_hex = (
            DEFAULT_COLOR[1:] if DEFAULT_COLOR.startswith("#") else DEFAULT_COLOR
        )
        return f"&H00{default_hex[4:6]}{default_hex[2:4]}{default_hex[0:2]}"


