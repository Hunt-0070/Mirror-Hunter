def validate_color_input(colors_raw: str):
    """Validate and return parsed colors from user input.

    Args:
        colors_raw (str): Raw color input string (space or pipe-separated).

    Returns:
        Tuple[List[str], List[str]]: Valid colors and invalid colors.
    """
    colors, invalid_colors = [], []
    if colors_raw:
        sep = "|" if "|" in colors_raw else " "
        for c in colors_raw.split(sep):
            c = c.strip().lower()
            if not c:
                continue
            # Check if it's a color name first
            if c in COLOR_NAME_MAP:
                colors.append(COLOR_NAME_MAP[c])
            # Check if it's a hex color with #
            elif (
                c.startswith("#") and len(c) == 7 and re.match(r"^#[0-9A-Fa-f]{6}$", c)
            ):
                colors.append(c)
            # Check if it's a hex color without #
            elif re.match(r"^[0-9A-Fa-f]{6}$", c):
                colors.append("#" + c)
            else:
                invalid_colors.append(c)
    return colors, invalid_colors


def _format_time(ms: int, sub_format: str) -> str:
    if ms < 0:
        ms = 0
    total_seconds, m_seconds = ms // 1000, ms % 1000
    h = total_seconds // 3600
    m = (total_seconds % 3600) // 60
    s = total_seconds % 60
    if sub_format == "srt":
        return f"{h:02d}:{m:02d}:{s:02d},{m_seconds:03d}"
    elif sub_format in ["vtt", "webvtt", "sami", "ttml"]:
        return f"{h:02d}:{m:02d}:{s:02d}.{m_seconds:03d}"
    elif sub_format == "ass":
        return f"{h}:{m:02d}:{s:02d}.{m_seconds // 10:02d}"
    else:
        return f"{h:02d}:{m:02d}:{s:02d}.{m_seconds:03d}"


def _parse_time(time_str: str) -> int:
    if not time_str:
        return 0
    time_str = time_str.replace(",", ".")
    parts = time_str.split(":")
    if len(parts) != 3:
        return 0
    try:
        h, m = int(parts[0]), int(parts[1])
        s_parts = parts[2].split(".")
        s = int(s_parts[0])
        ms = int(s_parts[1].ljust(3, "0")[:3]) if len(s_parts) > 1 else 0
        return (h * 3600 + m * 60 + s) * 1000 + ms
    except (ValueError, IndexError):
        return 0


def _shift_timestamps(content: str, offset_ms: int, sub_format: str) -> str:
    if not content or offset_ms <= 0:
        return content
    if sub_format == "ass":
        ass_pattern = re.compile(
            r"^(Dialogue: .*?,)(\d:\d{2}:\d{2}\.\d{2})(,)(\d:\d{2}:\d{2}\.\d{2})(,.*)",
            flags=re.MULTILINE,
        )

        def repl(match):
            start_ms = _parse_time(match.group(2)) + offset_ms
            end_ms = _parse_time(match.group(4)) + offset_ms
            start = _format_time(start_ms, "ass")
            end = _format_time(end_ms, "ass")
            return f"{match.group(1)}{start}{match.group(3)}{end}{match.group(5)}"

        return ass_pattern.sub(repl, content)
    time_pattern = re.compile(r"(\d{1,2}:\d{2}:\d{2}[,.]\d{2,3})")

    def replacer(m):
        original_ms = _parse_time(m.group(1))
        new_ms = original_ms + offset_ms
        return _format_time(new_ms, sub_format)

    return time_pattern.sub(replacer, content)


def _renumber_srt(content: str, add: int) -> str:
    if add <= 0 or not content:
        return content
    lines = content.splitlines()
    result_lines = []
    for line in lines:
        stripped_line = line.strip()
        if stripped_line.isdigit():
            result_lines.append(str(int(stripped_line) + add))
        else:
            result_lines.append(line)
    return "\n".join(result_lines)


