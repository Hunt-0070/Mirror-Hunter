def build_intro_replace_cmd(
    video_path: str, mod_sub_path: Path, sub_to_replace: dict, all_streams: list
):
    if not os.path.exists(video_path) or not mod_sub_path.exists():
        return None, None
    p = Path(video_path)
    out_path = str(p.with_name(f"{p.stem}_intro.mkv"))
    cmd = [
        BinConfig.FFMPEG_NAME,
        "-hide_banner",
        "-y",
        "-loglevel",
        "error",
        "-i",
        video_path,
        "-i",
        str(mod_sub_path),
    ]
    map_args, metadata_args, output_sub_index = [], [], 0
    for stream in all_streams:
        if stream["index"] == sub_to_replace["index"]:
            map_args.extend(["-map", "1:0"])
            tags = stream.get("tags", {}) or {}
            if tags.get("language"):
                metadata_args.extend(
                    [
                        f"-metadata:s:s:{output_sub_index}",
                        f"language={tags['language']}",
                    ]
                )
            if tags.get("title"):
                metadata_args.extend(
                    [f"-metadata:s:s:{output_sub_index}", f"title={tags['title']}"]
                )
            metadata_args.extend(
                [f"-disposition:s:{output_sub_index}", "default+forced"]
            )
            output_sub_index += 1
        else:
            map_args.extend(["-map", f"0:{stream['index']}"])
            if stream.get("codec_type") == "subtitle":
                output_sub_index += 1
    cmd.extend(map_args)
    cmd.extend(["-c", "copy"])
    cmd.extend(metadata_args)
    cmd.extend(["-progress", "pipe:1"])
    cmd.append(out_path)
    return cmd, out_path


async def _get_video_resolution(video_path: str):
    """Get video resolution from file."""
    try:
        cmd = [
            BinConfig.FFPROBE_NAME,
            "-v",
            "quiet",
            "-print_format",
            "json",
            "-show_streams",
            video_path,
        ]
        proc = await _create_memory_aware_subprocess(
            *cmd, stdout=PIPE, stderr=PIPE, max_retries=1, wait_for_resources=False
        )

        if not proc:
            LOGGER.error(
                f"Failed to create subprocess for video resolution check: {video_path}"
            )
            return 720, 1280  # Default resolution

        out, _ = await proc.communicate()
        if proc.returncode == 0:
            data = json.loads(out)
            for s in data.get("streams", []):
                if s.get("codec_type") == "video":
                    w = s.get("width")
                    h = s.get("height")
                    if w and h and int(w) > 0 and int(h) > 0:
                        return int(w), int(h)
    except Exception as e:
        LOGGER.debug(f"Could not get video resolution: {e}")
    return INTRO_SUB_RES_X, INTRO_SUB_RES_Y


